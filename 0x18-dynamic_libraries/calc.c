#include "main.h"
/**
 * add - returns the addition of two nums.
 * @a: first num.
 * @b: second num.
 *
 * Return: the sum of a and b.
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - returns the subtraction of two nums.
 * @a: first num.
 * @b: second num.
 *
 * Return: the sub of a and b.
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - returns the multiplication of two nums.
 * @a: first num.
 * @b: second num.
 *
 * Return: the mul of a and b.
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - returns the divition of two nums.
 * @a: first num.
 * @b: second num.
 *
 * Return: the div of a and b.
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - returns the modules of two nums.
 * @a: first num.
 * @b: second num.
 *
 * Return: the module of a and b.
 */
int mod(int a, int b)
{
	return (a % b);
}
