#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>
/**
 * op_add - addition functions
 * @a: input one
 * @b: input two
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction functions
 * @a: input one
 * @b: input two
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication functions
 * @a: input one
 * @b: input two
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division functions
 * @a: input one
 * @b: input two
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - module functions
 * @a: input one
 * @b: input two
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
